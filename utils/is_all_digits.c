#include "../philo.h"

int	is_digit(char c)
{
	return (c >= '0' && c <= '9');
}

int	is_all_digits(char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (!is_digit(s[i]))
			return (0);
		i++;
	}
	return (1);
}
